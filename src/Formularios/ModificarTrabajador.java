    private void cbo_MRutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbo_MRutActionPerformed
        String buscarRut=cbo_MRut.getSelectedItem().toString();
        Trabajador borrador=c.mostrarTrabajador(buscarRut);
        txt_MNombre.setText(borrador.getNombre());
        txt_MDireccion.setText(borrador.getDireccion());
        txt_MSueldoBase.setText(borrador.getSueldoBase());
        cbo_MCiudad.setSelectedIndex(borrador.getCiudad());
        cbo_MCargo.setSelectedIndex(borrador.getCargo());
        if(borrador.getSexo().equalsIgnoreCase("Masculino")){
                rbtn_MMasculino.setSelected(true);
            }else{
                rbtn_MFemenino.setSelected(true);
            }
        habilitar();
        
    }//GEN-LAST:event_cbo_MRutActionPerformed

